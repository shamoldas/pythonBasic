# Python code to demonstrate the Application of 
# min() and max() 

# printing the word occuring 1st among these in dict. 
# "geeks", "manjeet", "algorithm", "programming" 
print("The word occuring 1st in dict. among given is : ",end="") 
print (min( "geeks", "manjeet", "algorithm", "programming" ) ) 

# printing the word occuring last among these in dict. 
# "geeks", "manjeet", "algorithm", "programming" 
print("The word occuring last in dict. among given is : ",end="") 
print (max( "geeks", "manjeet", "algorithm", "programming" ) ) 
